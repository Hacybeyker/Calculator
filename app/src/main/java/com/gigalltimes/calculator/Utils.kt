package com.gigalltimes.calculator

fun suma(numberOne: Double, numberTwo: Double): Double = numberOne.plus(numberTwo)
fun resta(numberOne: Double, numberTwo: Double): Double = numberOne.minus(numberTwo)
fun multiplicacion(numberOne: Double, numberTwo: Double): Double = numberOne.times(numberTwo)
fun division(numberOne: Double, numberTwo: Double): Double = numberOne.div(numberTwo)
